public class Variables {

    public static void main(String[] args) {
        
        char a = 'R';
        System.out.println(a);
        
        char b = 'A';
        System.out.println(b);
        
        String c = "C++";
        System.out.println(c);
        
        int x = 6;
        System.out.println(x);
        
        int y = -12;
        System.out.println(y);
        
        int z = 40000;
        System.out.println(z);
        
        float d = 3.14159f;
        System.out.println(d);

    }
}
